    public List<Sp500WeightedConstituent> getConstituentsFromFile() {
        File dataFile = new File(BASE_FOLDER + "/info/sp500_constituent.json");

        List<Sp500Constituent> constituents = DataLoader.readListOfClassFromFile(dataFile, Sp500Constituent.class);

        long totalMarketCap = 0;

        for (var con : constituents) {
            Optional<AtGlanceData> data = symbolIndexProvider.getAtGlanceData(con.symbol);
            if (data.isPresent()) {
                totalMarketCap += data.get().marketCapUsd;
            } else {
                System.out.println("Unable to find symbol for " + con.symbol);
            }
        }

        List<Sp500WeightedConstituent> result = new ArrayList<>();
        for (var con : constituents) {
            Optional<AtGlanceData> data = symbolIndexProvider.getAtGlanceData(con.symbol);
            if (data.isPresent()) {
                double weight = data.get().marketCapUsd / totalMarketCap;
                result.add(new Sp500WeightedConstituent(con.symbol, weight));
            } else {
                System.out.println("Unable to find symbol for " + con.symbol);
            }
        }

        Collections.sort(result, (a, b) -> Double.compare(b.weight, a.weight));

        return result;
    }

